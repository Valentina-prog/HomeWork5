package oop;

public class Animal {

    private String childFriendlyAnimal;
    private String name;
    private float age;

    public String getChildFriendlyAnimal() {
        return childFriendlyAnimal;
    }
    public void setChildFriendlyAnimal(String childFriendlyAnimal){
        this.childFriendlyAnimal = childFriendlyAnimal;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getAge() {
        return age;
    }

    public void setAge(float age) {
        this.age = age;
    }
}




